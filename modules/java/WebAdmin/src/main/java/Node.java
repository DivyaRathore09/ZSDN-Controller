package RESTAdmin;

/**
 * @author Maksim Pahlberg
 * Container for topo node data
 * used with GSON
 *
 */
public class Node {
	String name;
	String id;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	
	

}
